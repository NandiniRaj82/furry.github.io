const searchButton = document.getElementById("search-btn");
searchButton.addEventListener("click", () => {
  const searchTerm = document.getElementById("search-input").value.trim();
  const url = `https://api.api-ninjas.com/v1/animals?name=${searchTerm}`;
  const headers = { 'X-Api-Key': 'YV6acWd733ZyxIJsjV3BYQ==9pWJoYVYfdRtwcte' };

  fetch(url, { headers })
    .then(response => {
      if (!response.ok) {
        throw new Error("Network response was not OK");
      }
      return response.json();
    })
    .then(data => {
      const animal = data[0];
      const details = `
      <!DOCTYPE html>
      <html lang="en">
      <head>
        <meta charset="UTF-8">
        <meta name="viewport" content="width=device-width, initial-scale=1.0">
        <title>Animal Details</title>
        <link rel="stylesheet" href="style.css">
      </head>
      <body>
      <div class="animal-container">
      <div class="animal-card">
      <div class="flex">
      <div><img src="back.jpg" class=ullu></div>
      <div class="text-container"> 
        <h1 class="animal-name">${animal.name ? animal.name : "N/A"}</h1>
        <p class="location">Location: ${animal.locations ? animal.locations.join(", ") : "N/A"}</p>
        <p  class="char">Characteristics:</p>
        <ul>
        ${animal.characteristics ? (
          Object.entries(animal.characteristics).map(([key, value]) => {
            if (value !== undefined) {
              return `<li class ="list10"><strong>${key}:</strong> ${value}</li>`;
            }
          }).join("")
        ) : ""}
  </ul>
      ${
          animal.scientific_name
            ? `<ul><li class ="list10">Scientific Name: ${animal.scientific_name}</li></ul>`
            : ""
        }
            </ul>
            </div>
            </div>
          </div>
        </div>
      </body>
      </html>
    `;
      const newTab = window.open("", "_self");
      newTab.document.write(details);
    })
    .catch(error => {
      console.error("There was a problem with your fetch request: ", error);
    });
});

